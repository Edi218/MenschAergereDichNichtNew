package spiel;

public class Figure {
    public int id;
    public int color;
    public int position;

    public void setColor(final int color){
        this.color = color;
    }
    public int getColor(){
        return color;
    }

    public void setId(final int id){
        this.id = id;
    }
    public int getId(){
        return id;
    }

    public void setPosition(final int position){
        this.position = position;
    }
    public int getPosition(){
        return position;
    }


}
